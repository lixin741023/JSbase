// null 和 undefined

// null：
// 表示一个特殊值。
// 常用来描述：空值 或 值为空。
// 大概表示，字符串，对象，数字是 无值 的。

// undefined：
// 同样表示值的空缺，用未定义的值，来表示更加 深层次 的 空值。
// 它是变量的一个取值，表明变量没有 初始化。
// 对某个对象，数组进行取值的时候若返回了undefined，则说明这个属性或元素不存在。
// 没有设置函数的返回值，函数默认返回 undefined。

// 主要识别：
// 可以认为：undefined 表示系统级别的，出乎意料的，或类似错误的值的空缺，
// <|而 null表示，程序级别的，正常的，或在意料之中的值的空缺。
// 所以：
// <|如果想要把 它们 赋值给某个变量、或者属性，或将它们作为参数传入函数，最佳的选择是使用 null。

